# we can use direct variables in the f string in python 3.6 and above

name = "yasra"
city = "karachi"

# we can use f string to print the variables in a formatted way
print(f"my {name} is from {city}")  # this will print "my name is from karachi"
# we can also use expressions in the f string
print(
    f"the length of my name is {len(name)}"
)  # this will print "the length of my name is 5"

